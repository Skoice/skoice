package net.clementraynaud.skoice.commands;

import net.clementraynaud.skoice.commands.arguments.Argument;
import org.bukkit.command.Command;
import org.bukkit.command.CommandExecutor;
import org.bukkit.command.CommandSender;
import org.jetbrains.annotations.NotNull;

public class SkoiceCommand implements CommandExecutor {

    @Override
    public boolean onCommand(@NotNull CommandSender sender, @NotNull Command cmd, @NotNull String label, String[] args) {
        if (args.length == 0 || Argument.Option.getOption(args[0]) == null) {
            return true;
        }
        Argument.Option.getOption(args[0]).run(sender, args.length == 1 ? "" : args[1]);
        return true;
    }
}
